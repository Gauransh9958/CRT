#include <iostream>
using namespace std;

int main() {
    long long X, Y;
    
    // Input the two numbers
    cin >> X >> Y;
    
    // Output the summation, multiplication, and subtraction
    cout << X<<" + "<<Y<<" = " << X + Y << endl;
    cout << X<<" * "<<Y<<" = " <<X*Y<< endl;
    cout << X<<" - "<<Y<<" = "<< X - Y << endl;
    
    return 0;
}
